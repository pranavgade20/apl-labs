package week2._7;

public class Multiply {
    public Multiply(int x, int y) {
        System.out.println(x + " times " + y + " is: " + x * y);
    }
    public Multiply(int x, int y, int z) {
        System.out.println(x + " times " + y + " times " + z + " is: " + x * y * z);

    }
    public Multiply(double x, double y) {
        System.out.println(x + " times " + y + " is: " + x * y);
    }
}
